
public class SwitchCase {

	public static void main(String[] args) {
		int num = 50;
	
switch(num) {

case 50:System.out.println("this is 50");
break;
case 10:System.out.println("this is 50");
break;
case 20:System.out.println("this is 50");
break;
case 30:System.out.println("this is 50");
break;
case 40:System.out.println("this is 50");
break;
default:System.out.println("Invalid");
break;
	}

}
}